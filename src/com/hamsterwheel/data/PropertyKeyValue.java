package com.hamsterwheel.data;

public class PropertyKeyValue {

	private String key;
	private String value;
	
	public PropertyKeyValue(String key, String value) {
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
	
}